# ncrement / Decrement
# Take a number n and increment it by 1, then decrement it by 1 using operators.
print("Incrementing numbers from 1 to 5")
n = 1
while n <= 5:
    print(n)
    n += 1  # increment n by 1
print("Decrementing numbers from 1 to 5")
n = 5
while n >= 1:
    print(n)
    n -= 1  # decrement n by 1